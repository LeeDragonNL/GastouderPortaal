package com.example.opt3.Model;

public class Omzet {
    double loon;
    double totaleOmzet;
    int totaleUren;

    public double getLoon() {
        return loon;
    }

    public void setLoon(double loon) {
        this.loon = loon;
    }

    public double getTotaleOmzet() {
        return totaleOmzet;
    }

    public void setTotaleOmzet(double totaleOmzet) {
        this.totaleOmzet = totaleOmzet;
    }

    public int getTotaleUren() {
        return totaleUren;
    }

    public void setTotaleUren(int totaleUren) {
        this.totaleUren = totaleUren;
    }


}
